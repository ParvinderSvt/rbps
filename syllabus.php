The school endeavours to provide broad and liberal education in the initial years, the activity based learning is emphasized to develop communicative skills and to provide a strong base in main subjects i.e. English, Science and Mathematics. The school follows the NCERT curriculum at various levels as outlined below:</p>

<h3><strong>Pre-Primary :</strong></h3>

<hr />
<p>A variety of activities are offered for the enhancement of cognitive, physical and interactive skills of the child. The approach is theme based so that all activities are well integrated with the curriculum. Learning is made interesting, fun and activity oriented at this level. Participation of the students is encouraged through such activities as drawing and coloring, writing, group games, clay- modelling, puzzles, flash cards and conversations.</p>

<p>Poems and stories are dramatized using expressive actions. Focus is on developing communication skills and on reinforcing an innate sense of discovery in everything that is taught.</p>

<p>Children learn to comprehend through a multi sensory approach as concepts in Number Work, English and E.V.S. are made clear with the use of multimedia Nature walks and regular educational tours to the Zoos, Railway Museums and Game Parks develop a sense of openness towards the world.</p>

<h3><strong>Primary :</strong></h3>

<hr />
<p>A thematic approach is designed to stimulate the children through various inter-active sessions and activities. An extensive use of technology supplements classroom teaching. Project works in class calls for mass participation. An equal emphasis is laid on developing a strong sense of individuality. Field trips and excursions make learning fun.<br />
<br />
Use of the newspaper articles, quizzes, crosswords and other learning tools enhance general awareness.</p>

<h3><strong>Secondary :</strong></h3>

<hr />
<p>The emphasis is on acquiring in-depth conceptual knowledge. The curriculum is research oriented and based on teamwork vis-&agrave;-vis discussions, group project work, discovery and exploration. Personality development, value education and coping with peer pressure are stressed upon at this stage. Students work in groups and make power point presentations and videos on subject related topics.</p>

<h3><strong>Senior &ndash; Secondary :</strong></h3>

<hr />
<p>Science and Commerce are the main academic streams offered at Simpkins.<br />
<br />
Various combinations of subjects are offered so that students can pursue their desired courses of study later in life. Result oriented training is imparted by experienced professionals who target for total development and desired success.<br />
<br />
The concept of Synchro-Study has been introduced to reduce stress levels of students. A synchronized study pattern is maintained between the course content of the board examinations and that of the competitive entrance examinations. Students have complete access to the various laboratories and library resources, specially designed for the study and research requirements of the senior level students.</p>

<h3><strong>Evaluation :</strong></h3>

<hr />
<p>The Central Board of Secondary Education (CBSE) has implemented the scheme of Continuous and Comprehensive Evaluation (CCE) for all students till class X. CCE primarily aims at bringing a paradigm shift from examination centered learning to holistic education. It not only intends to make assessment an integral part of the teaching &ndash; learning process, but also improves class-room deliberations resulting in meaningful and productive learning. In tune with the CBSE policy, it is our belief that evaluation should act as a positive input for improving the teaching learning process and not as a deterrent. The report card of evaluation here reflects not only the scholastic aspects of the learner, but also life skills acquisition, personality attributes, behaviour, interests, attitudes, values and proficiency in indoor as well as outdoor co-curricular activities.</p></div>
